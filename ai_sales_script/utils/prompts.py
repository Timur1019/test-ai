def build_prompt(call_transcripts):
    calls_text = "\n\n---\n\n".join(call_transcripts)
    return f"""
На основе следующих успешных звонков:

{calls_text}

Сформируй шаблон скрипта продаж с разбивкой по этапам:
- Приветствие
- Выявление потребности
- Предложение решения
- Закрытие (демо/встреча)

Также объясни:
- Почему такая структура?
- Какие триггеры используются?
- Какие фразы работают лучше всего?
"""
